int i, j;

void eq() {
	if(i == j)
		i = 0;
	else
		i = 1;
}

void ne() {
	if(i != j)
		i = 0;
	else
		i = 1;
}

void lt() {
	if(i < j)
		i = 0;
	else
		i = 1;
}

void le() {
	if(i <= j)
		i = 0;
	else
		i = 1;
}

void gt() {
	if(i > j)
		i = 0;
	else
		i = 1;
}

void ge() {
	if(i >= j)
		i = 0;
	else
		i = 1;
}
